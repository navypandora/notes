Netmask tells us our network name: for eg. if our IP is 192.168.1.2
and our Netmask is 255.255.255.0 then our network name is 192.168.1.0
network name: the range of IP addresses the system knows
for eg. 192.168.1.0-255
Now if a system wants to connect to other system with a different network name it should go through the gateway
which is the router in most cases.


Connect IPs with same network name: switch
Connect IPs with different network name: router
